% --- Executes on button press in radiobutton3.
function radiobutton3_Callback(hObject, eventdata, handles)
% hObject    handle to radiobutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Hint: get(hObject,'Value') returns toggle state of radiobutton3
    global audio;
    global audioFs;
    global now_audio;
    %audioFs=441000;
    handles.audio_62 = fx_FIR( 62/sqrt(2),62*sqrt(2), audio);
    handles.audio_125 = fx_FIR( 125/sqrt(2) ,125*sqrt(2) , audio);
    handles.audio_250 = fx_FIR( 250/sqrt(2) ,250*sqrt(2) , audio);
    handles.audio_500 = fx_FIR( 500/sqrt(2) ,500*sqrt(2) , audio);
    handles.audio_1K = fx_FIR( 1000/sqrt(2),1000*sqrt(2), audio);
    handles.audio_2K = fx_FIR( 2000/sqrt(2),2000*sqrt(2), audio);
    handles.audio_4K = fx_FIR( 4000/sqrt(2),4000*sqrt(2), audio);
    handles.audio_8K = fx_FIR( 8000/sqrt(2) ,8000*sqrt(2) , audio);
    handles.audio_16K = fx_FIRH( 16000/sqrt(2), audio);
    % guidata(hObject, handles);

    %获取各个滑动条的位置
    
    A = get(handles.Freq62 , 'Value');
    B = get(handles.Freq125 , 'Value');
    C = get(handles.Freq250 , 'Value');
    D = get(handles.Freq500 , 'Value');
    E = get(handles.Freq1K , 'Value');
    F = get(handles.Freq2K , 'Value');
    G = get(handles.Freq4K , 'Value');
    H = get(handles.Freq8K , 'Value');
    I = get(handles.Freq16K , 'Value');

    a=10^(A/20);
    b=10^(B/20);
    c=10^(C/20);
    d=10^(D/20);
    e=10^(E/20);
    f=10^(F/20);
    g=10^(G/20);
    h=10^(H/20);
    i=10^(I/20);
    %各个音频信号相加，乘以权重，乘以音量增益，绘制此时的信号的频谱
    now_audio = a*handles.audio_62 + b*handles.audio_125 + c*handles.audio_250 + d*handles.audio_500 + e*handles.audio_1K + f*handles.audio_2K + g*handles.audio_4K+h*handles.audio_8K+i*handles.audio_16K;
    Audio_ff = abs(fft( now_audio ));

    sound_play(now_audio,audioFs,0.01); 
    subplot(1,2,2);plot( Audio_ff );axis([0,audioFs,0,20000]);
    
    guidata(hObject, handles);



